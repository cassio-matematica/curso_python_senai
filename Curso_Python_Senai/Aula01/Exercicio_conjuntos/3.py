##Exercício 3: Verificar interseção
##Escreva um programa que declare dois conjuntos de frutas (conjunto1 e conjunto2) e exiba as frutas que estão presentes em ambos os conjuntos.

conjunto1 = {'maçã', 'banana', 'laranja'}
conjunto2 = {'banana', 'abacaxi', 'uva'}
intersecao = conjunto1.intersection(conjunto2)
print("Frutas em comum:", intersecao)

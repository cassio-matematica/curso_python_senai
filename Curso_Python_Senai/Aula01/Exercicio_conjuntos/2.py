##Exercício 2: União de conjuntos
##Escreva um programa que declare dois conjuntos de cores (conjunto1 e conjunto2) e exiba a união desses conjuntos.

conjunto1 = {'vermelho', 'azul', 'verde'}
conjunto2 = {'azul', 'amarelo', 'roxo'}
uniao = conjunto1.union(conjunto2)
print("União dos conjuntos:", uniao)
